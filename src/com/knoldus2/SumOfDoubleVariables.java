package com.knoldus2;

/* Create a program that declares and initializes two double variables named "num1" and "num2" with the values 3.14 and 6.28 respectively.
   Calculate the sum of these two variables and print the result. */

public class SumOfDoubleVariables {
    public static void main(String[] args) {
        double num1=3.14, num2=6.28;
        double sum=num1+num2;
        System.out.println("Sum is: "+sum);
    }
}
